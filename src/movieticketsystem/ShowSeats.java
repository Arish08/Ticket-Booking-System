    void checkingallseats() {
        int a[] = F.readtempdetails();
        String s[][] = new String[seatlength][4];
        int[][] p = new int[seatlength][4];
        
        s = F.readworddetails("Seats.txt", 0, seatlength, 4);
        for (int j = 0; j < seatlength; j++) {
            for (int k = 0; k < 4; k++) {
                p[j][k] = Integer.parseInt(s[j][k]);
            }
        } 
        for (int j = 0; j < seatlength; j++) {
            for (int k = 0; k < 4; k++) {
                int pk1 = p[j][0], pk2 = p[j][1], pk3 = p[j][2], pk4 = p[j][3];
                MovieSeat[pk1][pk2][pk3][pk4] = 1;
            }
        }
        int a0 = a[0], a1 = a[1], a2 = a[2];
        for (int i = 0; i < 20; i++) {
            if (MovieSeat[a0][a1][a2][i] == 1) {
                buttonarray(i);
            }
        }
        jLabel1.setText("You Have Booked");
    }
    
